#Entrada dos dados
a=float(input("digite um número: "))
b=float(input("digite outro número: "))

#Processamento dos dados
s=a+b #soma
sub=a-b #subtração
m=a*b #multiplicação
d=a/b #divisão
p=a**b #potenciação

#Saída dos resultados
print(f"{a} + {b} = {s}")
print(f"{a} - {b} = {sub}")
print(f"{a} * {b} = {m}")
print(f"{a} / {b} = {d}")
print(f"{a} ** {b} = {p}")